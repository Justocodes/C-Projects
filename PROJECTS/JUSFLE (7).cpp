// This Progrman illustrates how integral expressions are
//evaluated.

#include <iostream>

using namespace std;

int main()
{
	cout << "2 + 5 = " << 2 + 5 << endl;
	cout << "the addition of two integers result in an integer" << endl;
	cout << "13 + 89 = " << 13 + 89 << endl;
	cout << "Likewise the addition of two integers resulting in an integer" << endl;
	cout << "34 - 20 = " << 34 - 20 << endl;
	cout << "the subtraction of two integers result in an integer" << endl;
	cout << "45 - 90 = " << 45 - 90 << endl;
	cout << "the subtraction of two integers resulting in a negative integer" << endl;
	cout << "2 * 7 = " << 2 * 7 << endl;
	cout << "the multiplication of two integers resulting in an integer" << endl;
	cout << "5 / 2 = " << 5 / 2 << endl;
	cout << "the division of two integers result in a quotient" << endl;
	cout << "14 / 7 = " << 14 / 7 << endl;
	cout << "the division of two integers resulting in a quotient " << endl;
	cout << "34 % 5 = " << 34 % 5 << endl;
	cout << "the mod of two integers resulting in a remainder " << endl;
	cout << "4 % 6 = " << 4 % 6 << endl;
	cout << "the mod of two intergers resulting in a remainder" << endl;
	system("pause");
	return 0;
}
